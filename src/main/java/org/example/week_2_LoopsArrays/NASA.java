package org.example.week_2_LoopsArrays;

public class NASA {
    public static void main(String[] args){

        // The starting integer will be 10
        // x >= 0 indicates where the loop will stop
        // x-- indicates that the numbers will decrease
        for(int x = 10 ; x >= 0 ; x--){
            System.out.println(x);
        }
    }
}

